# Python comments
# Comments can be used to explain Python code.
# Comments can be used to make the code more readable.
# Comments can be used to prevent execution when testing code.

# Createing a Commnet

# This is a commnet
print("Hello, World!")

print("Hello, World!") # This is a comment

# Multiple Comments
# Python does not really have a syntax for multi-line comments. To add a multiline comment you could insert a # for each line:
# since python ignores strings that are not assigned to a variable, you can also use a multiline string (triple quotes) as a comment.

"""
This is a comment
written in
more than just one line
"""
print("Hello, World!")