#Python Output / Print

# Print Text
print("Hello, World!")

# Double Quotes
# Text in Python must be inside quotes. You can use either " double quotes or ' single quotes.

print("Hello, World!")
print('Hello, World!')

# Print without a New Line
# By default, the print() function adds a new line at the end of the output. To avoid this, you can use the end parameter.
print("Hello, World!", end=" ")
print("I Will print on the same line.")



# Print Numbers
# However, unlike text, we don't put numbers inside quotes. 

print(5)
print(10)
print(15)

print(3 + 10)
print(4 * 20)

# Mix Text and Numbers
# You can combine text and numbers in one output by separating them with comma:
print("I am", 27, "Years old.")